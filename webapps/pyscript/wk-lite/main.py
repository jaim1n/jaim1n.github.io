def bubble_text(text):
    lines = text.split('\n')
    max_length = max(len(line) for line in lines)
    bubble_width = max_length + 4
    bubble = []
    bubble.append("  " + "_" * bubble_width)
    for line in lines:
        bubble.append(" / " + line.ljust(max_length) + " \\")
    bubble.append(" \\_" + "_" * max_length + "_/")
    return "\n".join(bubble)

